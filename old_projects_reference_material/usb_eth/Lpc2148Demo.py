
from Tkinter import *
import socket
import os
import re

USB_INTERFACE = "usb0"
CONFIGURE_IP = "10.3.0.0"
CONTROL_IP = "10.3.0.1"
CONTROL_PORT = 23


class Lpc2148Controller(object):
    def __init__(self, address, port):
        self.control_socket = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        self.control_socket.connect((address , port))
        self.control_socket.recv(64)

    def close(self):
        self.control_socket.close()

    def lcd(self, msg):
        self.control_socket.send("lcd %s\n" % (msg,))
        self.control_socket.recv(16)
    def matrix(self, msg):
        self.control_socket.send("matrix %s\n" % (msg,))
        self.control_socket.recv(16)
    def motor(self):
        self.control_socket.send("motor\n")
        self.control_socket.recv(16)
    def rgb(self, color):
        self.control_socket.send("rgb %s\n" % color)
        self.control_socket.recv(16)
    def temperature(self):
        self.control_socket.send("temperature\n")
        data = self.control_socket.recv(64)
        m = re.search("([0-9].*C)",data)
        if m:
            return m.group(1)
        else:
            return None
    

class Lpc2148GUI(object):
    def __init__(self, root, boarrd_cntr):
        self.root = root
        self.cntrl = boarrd_cntr
        self.create_ui()

    def lcd_cmd(self, event):
        msg = self.lcd_entry.get()
        self.lcd_entry.delete(0, END)
        self.cntrl.lcd(msg)
        
    def matrix_cmd(self, event):
        msg = self.matrix_entry.get()
        self.matrix_entry.delete(0, END)
        self.cntrl.matrix(msg)

    def rgb_cmd(self, color):
        if color == "grey":
            color = "off"
        self.cntrl.rgb(color)

    def temperature_cmd(self):
        temperature = self.cntrl.temperature()
        if temperature is not None:
            self.temperature_entry["state"] = NORMAL
            self.temperature_entry.delete(0, END)
            self.temperature_entry.insert(END, temperature)
            self.temperature_entry["state"] = DISABLED

    def motor_cmd(self):
        self.cntrl.motor()

    def interface_cmd(self):
        os.system("xterm -e \"bash -c 'telnet %s '\" &" % CONTROL_IP)
        

    def create_ui(self):
        self.root.title("LPC2148 virtual ethernet interface demo")
        self.root.resizable(0,0)
        
        canvas = Canvas(self.root, width = 350, height = 221)
        canvas.grid(row=0, columnspan=9)
        self.gif1 = PhotoImage(file = 'LPC2148_EDU_350x221-board.gif')
        canvas.create_image(0, 0, image = self.gif1, anchor = NW)

        l = Label(self.root, text = "Characters LCD: ")
        l.grid(row=1, column=0, sticky=W)

        self.lcd_entry = Entry(self.root, width=25)
        self.lcd_entry.grid(row=1, column=1, columnspan=8)
        self.lcd_entry.bind("<Return>", self.lcd_cmd)
        
        l = Label(self.root, text = "LED matrix: ")
        l.grid(row=2, column=0, sticky=W)

        self.matrix_entry = Entry(self.root, width=25)
        self.matrix_entry.grid(row=2, column=1, columnspan=8)
        self.matrix_entry.bind("<Return>", self.matrix_cmd)


        l = Label(self.root, text = "RGB LED: ")
        l.grid(row=3, column=0, sticky=W)

        for i,color in enumerate(["grey","red","blue","purple","green","yellow", "cyan", "white"]):
            def f(color):
                def g():
                    self.rgb_cmd(color)
                return g
            b = Button(self.root, bg=color, activebackground=color, command=f(color))
            b.grid(row=3, column=i+1, sticky=W, columnspan=1)
            

        l = Label(self.root, text = "Temperature sensor: ")
        l.grid(row=4, column=0, sticky=W)
        b = Button(self.root, text='Update', command = self.temperature_cmd)
        b.grid(row=4, column=1, sticky=W, columnspan=4)
        self.temperature_entry = Entry(self.root, width=6, state=DISABLED)
        self.temperature_entry.grid(row=4, column=4, sticky=W, columnspan=2)




        l = Label(self.root, text = "Step motor: ")
        l.grid(row=5, column=0, sticky=W)

        b = Button(self.root, text='Go', command=self.motor_cmd)
        b.grid(row=5, column=1, sticky=W, columnspan=2)

        l = Label(self.root, text = "Command line interface: ")
        l.grid(row=6, column=0, sticky=W)

        b = Button(self.root, text='Go', command=self.interface_cmd)
        b.grid(row=6, column=1, sticky=W, columnspan=2)


